/* Set the width of the side navigation to 250px */
function openNav() {
  document.getElementById("sidenav-bar").style.width = "250px";
}

/* Set the width of the side navigation to 0 */
function closeNav() {
  document.getElementById("sidenav-bar").style.width = "0";
}
